    <h1>Herschel Walker's Concession Speech Is A Lesson To All Would Be Politicians And Future Leaders</h1>
   <p>
	 <img src="images/herschelloss.png" />
   </p>
    <p>

Many will never know how difficult it is to run for office.  You have to have the courage to codify what you believe in, defend it verbally in public, and endure harsh criticism all along the way.  And then, there are no guarantees that you will win.  Even more disturbing is that if you win, you will continue to endure scrutiny.  You will continue to encounter people who don't believe what you believe or who just plain don't care for you at all or your ideas.  It is these reason, those considering office should take some time to listen to concession speeches by candidates who make it to the front of the pack and still end up losing.  It's when they lose and have to take the podium that we truly find out who they really are.   
<br><br>

Some candidates who lose choose to blame the system.  Some blame it on voter fraud.  Some will just never concede and really never accept the idea that their ideas, their campaign, or their candidacy was not in sync with the majority of the people.  You can hear clearly what a candidate really represents when they lose and have to stand before their peers and contemporaries to acknowledge they have losts.

<br><br>
For these reasons, Herschel Walker's concession speech should be a lesson to every would be elected official.  He gave a sincere, powerful lesson in how to concede.  He did not make excuses.  He did not change his beliefs.  He did not steer away from principles.  He thanked his family and all of his supporters. He pointed out that the election was about issues much bigger than himself and encouraged the people to believe in the Constitution.  He encouraged the people to continue to vote and to stand up for what they believe and to continue to dream.  What better message to leave with the people? <br><br>

You could argue that Herschel Walker's campaign and concession was a victory for the people.   Herschel bravely pointed out Warnock's shortcomings for all of Georgia and by doing so held him accountable to the people.  This will certainly have some influence on Senator Warnock when he votes on issues effecting Georgia in the future.  Herschel pointed out time and again how Senator Warnock was dodging issues during his campaign.  That's how you hold people accountable.  And he did all this with the most high profile politician both Democrat and Republican criticizing his candidacy, his experience, and his qualifications.  And yet, he did more for the people of Georgia and the country as a whole with his concession speech than many of the elected officials have done in their last term.
		
		

