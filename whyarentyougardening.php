		So, day in and day out we hear stories about how the coronavirus is crippling world economies.  We hear how people are becoming ill by the thousands.  We hear about the unfortunate deaths and the horrible new reality where we have to distance ourselves from those we love for their own safety.  We hear about the toilet paper shortages, the school closures, the #stayyourasshome movement, and now the <a href="https://www.theguardian.com/global-development/2020/mar/26/coronavirus-measures-could-cause-global-food-shortage-un-warns">impending possibility of a food shortage</a> due to farmers not being able to enlist the annual manpower necessary to sow their crops or harvest them.  My question is, why don't I see people outside in their yards gardening?  What the hell are they waiting on?  I don't think it's that hard to realize that we're going to have to become a lot more efficient. <br><br>

People are strolling down the aisles at Walmart, complaining that there are no romen noodles.  They're wearing long faces as they pass by the empty toilet paper racks.  They're complaining about not being able to buy a floor freezer to cram more food in.  But I don't see anyone outside with a shovel or a hoe in their hand.  That's right, get a hoe and go out to your back yard if you have one and bust the dirt up.  Rent a tiller or fix the broken one in your garage. What are you waiting on?  The crisis has arrived.<br><br>
<img src="images/cartoongarden/IMG_0477.JPG"/><br><br>
I'm sure many of you can recount stories told by your elders about large families who depended on farming for their food.  Families in the 50's, 60's, and 70's often stretched towards 10 people or more.  It was quite common.  And you know what, Walmart was not everywhere to save them.  Walmart began in 1950.  In 1962, Walmart opened its chain and it would take 33 years for Walmart to expand to every state in the US.  So, why is this important?  The point is, mom and pop stores (otherwise know as small businesses) supplied every town in America with the food, goods, and services they relied on.  The world has become over dependent on large corporations to provide their basic needs and this represents a fundamental weakness in our society.  We may have created three generations of people who have barely grown anything.  Worse yet, we may have some folks out there that cant' grow anything... And right now, that's a really bad thing.<br><br>

It's literally time for all of us to get back to our roots. We need to get really serious about putting a garden in.  It's time to pick up the shovel if you don't have a tiller and turn the dirt over.  It's time to get some seeds and some plants from your local feed and seed.  It's time to stop calling your yard manicure specialists and realize that there is nothing more beautiful than seeing food growing in your yard.  Stop worrying about how much you can't get in the freezer and take a look at having food laying on the ground.  <br><br><img src="images/cartoongarden/IMG_0476.JPG"/><br><br>Put some potatoes in the ground.  Plant some tomatoes.  Plant some corn and some bell peppers.  Plant some hot pepper too.  Don't forget the onions.  Stretch yourself out some pole beans.  Where are your squash and your cucumbers?  Where are your collards and your turnips?  Get yourself some cabbage out there?   Don't wait for the stores to close before you realize, damn, I should have planted a garden.  <br><br>

<br><br><img src="images/cartoongarden/IMG_0480.JPG"/><br><br>

