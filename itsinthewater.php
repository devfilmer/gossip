Don't get me wrong.  I'm glad they were there and executed their duties swiftly and broke up the fight before someone was seriously injured.  It's just how they executed their duties that bothers me.  Why is it that the black teen was thrown to the ground, knees pressed to his back, and then cuffed while the white teen was simultaneously told to sit on the couch.  Almost humorously, the white teen stuck his wrists in the air as if he wanted to be cuffed.  I'm not sure why.  Maybe he felt left out... But why was there a difference in how they were treated?  Look, I watched the fight.  No one was winning.  I think the white teen was actually even bigger.  Did they feel more threatened because there was a young black man fighting to protect himself?  Why were they not equally as threatened by the white teen.  Let's get the facts straight.  Oh yeah, I'm not going to leave anything out from what I saw.  Both cops were white.  Did that have something to do with it?  Maybe it's standard operating procedure for the New Jersey Police to put the black man on the ground and let the white man sit and explain what why they were doing what ever it is they were doing.  I'm just trying to understand.
