Isn’t it about time Americans had a serious conversation about gun control?  The events in Buffalo and Uvalde are beyond tragic and yet, it’s old news.  The shootings happen time and time again… in the schools, in the malls, in the church, in the subway, in the streets, at the baseball game, on facebook live, at night, in the middle of the day, from coast to coast, there’s no where safe in America.  So, what’s the real problem?  There have been some fascinating interviews taking place on mainstream media.  The major news networks appear to be looking for someone to blame.  Some are blaming the NRA.  Some are blaming Congress.  Others are blaming state law while still others blame federal laws.  Then, there are some blaming the gun manufacturers.  Some are blaming the system…  But when are people going to realize the real problem?  <br><br>
		
		 <img src="images/uvalde2.png" /><br><br>

America has confused the right to bare arms with an adoption of a culture of violence.  Our society focuses on physical violence as a means of solving problems, obtaining wealth, entertainment, and now a method of obtaining attention and notoriety.   America has embraced an instrument which has the sole purpose of killing as a symbol of power, an instrument of power, and the ultimate method of exacting one’s will.  The wielding of a gun has now become a staple part of American culture.  So, how do you fix a cultural adoption of a seriously bad habit?  I don’t have an answer yet for this one.  But, it is evident that the laws currently in place and the controls that currently followed by gun brokers is landing weapons in the hands of mentally unstable individuals who have no respect for the lives of the innocent.  And because of this, sitting still and watching one tragedy after another and another is not going to solve the problem.  You can’t hope it just all goes away or expect that it’s just something that will all go away soon.  If you believe that, you’re fooling yourself.  Let’s face it.  Vigilance is not enough.  Something must be done.  Soon!  The domino effect of this acidic violence could have catestrophic results down the line.  What happens when the people decide that tears shed for their loved ones are not enough?  What happens if they decide to take up arms?  It’s not a new notion.  An eye for an eye.  And though these victims should know that resorting to violence will only beget more violence, sanity and rational thought are often the first victims of extreme violence.  It’s instinct to want to protect yourself and the ones you love.  And this is where the problem is.  When someone kills the innocent.  When someone takes the life of our loved ones, our natural instinct is to lash out.  Find who is to blame and to seek out justice…. Is that not the American Way… Justice.   <br><br>


So, if we don’t want to see this vicious cycle continue to repeat itself, maybe it’s time to really look at the Second Amendment again and reinterpret what is should mean today.  Maybe it is time to raise the age for someone to be able to get a firearm.  Maybe the types of firearms that are available to protect oneself should be limited.  Maybe it’s time to invest more money and more personnel into the security of America’s school systems.  Maybe it’s time to stop pimping violence on TV, in the movies, and in our games.  Maybe it’s time we really look the problem straight in the mirror and tell ourselves, we’re better that this… And we’re going to do better. <br><br>


