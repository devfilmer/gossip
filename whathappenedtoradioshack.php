    <p>

		It doesn't seem like that long ago when you could ride down to your local strip mall or even the big mall and there it was standing there... The most beautiful building in the whole complex, filled with radio controlled toys, weird electronic devices, and hundreds of small electrical parts that you could build almost anything out of.  There were phones, antennas, radios, and black lights.... toy motorcycles, and gyroscopes... You didn't know what you might find.  And now, it's all gone...  Radio Shack has disappeared! <br><br>
		
		How is it that the world has entered the raspberry pi era and there is no radio shack selling it.  Or is there?....  A little research reveals that Radio Shack is now officially owned by Retail Ecommerce Ventures (REV).  You'll also find that Radio Shack was apparently reminted online.  But don't get excited.  The Radio Shack site online looks dazzling... but as soon as you start searching for things, you may be disappointed.  Simple things like relays and diodes that used to litter the shelves of any Radio Shack are hard to find on their website.  Sadly, a quick jump over to Amazon and you can find everything you thought would have been at the Radio Shack site.<br><br>
		
		Radio Shack was founded by Milton Deutschmann and Theodore Deutschmann in 1921.  The company enjoyed incredible growth for 40 years until it began to struggle in the 60s.  From there, Radio Shack did continue to grow slowly as new franchises were opened around the world.  In 2015, Radio Shack filed for bankrupcy.  Afterward, various entities claimed their rights to the Radio Shack brand at various locations around the world.    General Wireless Inc. bought Radio Shack in 2015 but then filed for bankruptcy in 2017.  And in 2020, Retail Ecommerce Ventures (REV) acquired Radio Shack. <br><br>
		
		<img src="images/radioshack2.png"/><br><br>
		
		So, it's clear that we're not the only ones that believe that Radio Shack had great potential.  So, why did it fail?  There's a great article that speaks to what caused Radio Shack's demise <a href = "https://www.investopedia.com/articles/investing/081315/5-reasons-why-radioshack-went-out-business.asp"> located here.</a>  The summary of this article by Andriy Blokhin states that Radio Shack faultered because of:<br>
		<ul>
			<li>1. High Store Concentration</li>
			<li>2. Online Competition</li>
			<li>3. Product Concentration</li>
			<li>4. Management Problems</li>
			<li>5. Financial Missteps</li>
        </ul><br><br>
		<img src="images/radioshack3.png"/><br><br>
		So, where does this leave us?  It appears that ultra tech stores that focus on do it yourself aren't in fashion anymore.  If you're into building things that require electronics, you have to go online.  Everyone, including what's left of Radio Shack is shifting to an online presence....  and mostly, that boils down now to find it on Amazon.... but one thing is for sure, I'll never forget going to the mall and making my short spin through Radio Shack... There's just something about a brick and mortar store and the thought of being able to build something yourself that I think will never be forgotten.
		
		

		
		
		
		
		
		
