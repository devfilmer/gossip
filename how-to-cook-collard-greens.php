<h2>How To Cook Collard Greens</h2><br><br>

There's always been alot of talk about how to cook collard greens.  Some folks just cut them up, boil them and add a little salt and pepper while others go through the trouble of adding fat back meat (otherwise known as ham hocks) or a smoked turkey wing.  What we want to do with this short article is to explore how to make the best collard greens and determine what makes them better than the rest.  We also want to look at easy collard green recipes that won't take too much time and also a few of the more exotic recipe variations that may require a lot more effort but are certain to please.  So let's jump right in and talk about how to make collard greens.<br><br>
<p>
	 <img src="collard-greens/JPEG/collard-greens-in-field.jpg" />
   </p>

How To Make Collard Greens

For most folks who have ever lived in the country, it's no secret how to make collard greens. First, if you have fresh collard greens growing, you need to know when they are ready to pick.  So, how long does it take to grow collard greens?  It's somewhere between 55 - 75 days.  What I look for is the size of the leaves.  If you have Georgia collard, they might be ready if they are about the size of your hand.  The larger the leaves, the less you have to pick. <br>
<p>
	 <img src="collard-greens/JPEG/collard-greens-ready-to-pick.jpg" />
   </p>	  
Also, you want to make a note of when the collards were treated for pests before picking.  Ensure you check the instructions for any pesticide you may have used to grow your greens.  Make sure the prescribed time for the pesticide to not effect you has passed.  So, how many collards should you pick to have a whole pot? Well, what I like to do is actually use the pot that I'm going to cook them in.  I pick the greens and fill the pot knowing that once cooked down, the pot will not be full.  Here's a tip.  If you fill the pot with the greens, you should have somewhere between one third to one half a pot of greens once they boil down.  Some would be tempted to pick two pots of greens so the that the pot cooks down to a full pot of greens.  This might not be what you really want to do... Why?  Well, the reason is that you might want to add a turkey wing to the greens or ham hocks or some other type of fat back meat.  You might even want to add other vegetables to give your greens the mistical flavors that people are looking for.   The greens by themselves are not what makes people crave them.  The secret is something called pot licker which we will talk about later.  But first, let's talk about an easy collard green recipe.<br>
	<p>
	 <img src="collard-greens/JPEG/IMG_0902.jpg" />
   </p>

<ul>Easy Collard Green Recipe
<li>
-  Pick One whole pot of greens fresh from the garden</li>

<li>-  Wash greens thoroughly.  Ensure they are clean and bug free.</li>

<li>-  Depending on the size of the pot, fill it to about 1/10 of the pot with water.</li>

<li>-  Add seasonings:  This can be salt, pepper, a little sugar if you like.  Some folks like to use season salt.  If you want the greens to have a little pop, add chopped jalepeno peppers... but not too many.  Believe it or not, this is the base of your pot licker.</li>

<li>-  Add the freshly washed greens to the pot and cook on medium heat until they are done.  How long does it take to cook collard greens?  And, how do know when the greens are done?  It takes about one and half hours to cook collard greens.  To see if they are done, take a fork, and sample them.  If the greens are tough, they are not ready.</li>

<li>-  Serve!</li>
</ul>

How To Make The Best Collard Green Recipes

Now, of course this is going to be a contentious topic.  So, we have set some parameters.  How do we determine what are the best collard greens?  How do we judge them?  Let's talk about it.  When we think about all the variations of collard green recipes, southern style collard greens always come up in the conversation...  It is a known entity.  People absolutely love them and just can't get enough of them.  I've been to several hole in the wall restaurants bragging about their fresh vegetables and southern style collard greens.  One thing I've noticed is that no one can seem to agree on what southern style really means.  If you go out and take a look at various southern style collard green recipes across the internet, you will find a concensus that reveals that southern style collard greens usually replace water with chicken broth in the bottom of the pot.  In addition, they add onions, vinegar, ham, ham hock, or bacon, garlic, and sugar.  <br>
<p>
	 <img src="collard-greens/JPEG/southern-style-collard-greens.jpg" />
   </p>	  
	  
The addition of these ingredients to the easy collard green recipe takes the collards to a whole new level.  Adding the onions and chicken broth changes the texture of the greens.  The ham or bacon add a meaty base to the greens which gives both flavor and dimension to the dish.  Last, the vinegar and sugar provides a complexity to the palette that can only be tempered by the garlic leaving us wondering why can't we do this at home?  Well we can.  We have to understand that the greens... I.E. the best greens don't stand alone.  They are surrounded by a supporting cast that compliment them.  Am I saying southern style collard greens are the best?  No, I'm saying that collards by themselves are no match for collards with a supporting cast.  Whether you choose to add the traditional southern style ingredients or other bold variations, either option will certainly beat the basic greens with no additional ingredients.  Call it collardgreenology. The Best Collard Greens have something in them besides collard greens and they alter the texture, flavor, and overall taste of the basic greens! Here is a basic southern style collard green recipe you may enjoy!<br><br>

	  <h2>How To Really Put Collard Greens In Another Dimension</h2><br><br>

So, we've talked about easy collard green recipes verses southern style.  We've also talked about how greens with a supporting cast usually win over a basic pot of greens.  So how do we punch them up yet another notch?  Try adding peppers!  There are a variety of different peppers that can really add dimension to the greens that you would have never imagined... jalepeno, banana, bell, and even roasted red peppers can give your greens a knock out punch that no one saw coming.  If you really want to break bad, try adding a different meat like steak or shrimp to the collard greens or even butter beans or white beans.  The combinations are endless.  It's all about finding a delectable balance between simplicity and exotic that takes the consumer to a new place and leaves a positive impression they can't forget.  So, I hope this article has shed some light on how to cook collard greens.  Pick them. Wash them.  Season them.  Surround them with a supporting cast.... and enjoy! 



		
		

