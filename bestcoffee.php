    <h1>The Best Coffee You Will Ever Have In Your Life...</h1>
   <p>
	 <img src="images/creamers/coffee1.png" />
   </p>
    <p style = "font-size:20px">

If you're like most Americans, a cup of coffee in the morning is perhaps one of the most important parts of how you get your day started.  For many of us, it's the only way to get our day started.  And it just might be one of the top three things we look forward to in any given day.  Now for those out there who don't believe that a cup of coffee is that important... it tells me that you might not be drinking the same cup of coffee I'm talking about.  I'm talking about a cup of coffee that literaly says good morning.  How are you?  Everything is going to be Ok... And guess what, I'm going to help you get going.  I'm talking about a cup of coffee that says hold up, wait one second... Let's enjoy this moment... Let's look out the window.  Let's read this mornings news... Then we can figure out what we're going to do with this day.  If your coffee is not doing this for you, you may want to check out the rest of this article. <br><br>



When it comes to coffee, there's no right way to make it.  Some people swear by grinding the beans fresh.  Others actually drink instant coffee.  Some like to play with their Keurig machines and K-cups (my favorite).  And the variety of coffees available are too many to name.  But there is a secret that most people don't really pay attention to and that is the focus of this article.  You see, I believe it's all about the creamer.  That's right.  While everyone is focusing on the brand, to grind or not to grind, Keurig or instant... I'm focusing on the creamer.  Now what you're about to hear might floor you but I don't want you to get it wrong.  I'm a fan of a great creamer.  I believe the best are in liquid form.  It doesn't take much in a cup to make your coffee pretty good.  Here are a few below. <br><br>

	 <img src="images/creamers/creamerchoices.png" />
<br><br>

But there's a really big secret to having the best coffee you will ever have in your life.  You're not going to believe it.  Truth is when I came across it, I was upset.  I had invested hundreds of dollars in coffee machines, coffee, cream, and sugar over the years and didn't know this.  It was very upsetting to tell you the truth.  I thought about all of the coffee I had drank and wished I had known this one simple thing...
<br><br>

<h2>Get Rid Of The Creamer!</h2>
<br><br>
Wait, what? Get rid of the Creamer?  That's right. Drop the Creamer.  There's something better... And that something is whipping cream.  I'm not sure why I tried it.  No one told me to do it.  I had heard people use it for baking.   Truth is, I stumbled across a small carton of it that looked like it was about to spoil and I looked at the ingredients and realized it looked, smelled, and resembled the coffee creamers I enjoyed so much.... And oh yeah, I was out of creamer.  Of course most of us have had coffee with milk and that's pretty good... But let me tell you something, I dare you to put about 1/6th of your coffee cup filled with whipping cream, two teaspoons of sugar, and then filled with the coffee of your choice... I dare you.  Make sure it's good and hot.  I.E. Your whipping cream came out of the refrigerator so hit your coffee with 30 seconds in the microwave after you brew it, pore it and add the creamer.  I like putting my whipping cream and sugar in the cup first, then brew the coffee with a Keuring.  Then, I place the cup in the microwave for 30 seconds.  The result is a rich cup of coffee that it is really hard to describe.  It's almost like there's something missing in those expensive creamers we love so much... Once you've tried this, it may be hard for you to go back.  I do miss some of the more exotic creamers but some how they just don't hold a candle to whipping cream.<br><br>
	 <img src="images/creamers/whippingcreamchoices.png" />
<br><br>

So, if you've been waiting for a secret to share with your friends that will put a smile on their faces... You've got one now.  Make them a cup of coffee using whipping cream instead of the fancy creamers and serve it... Don't tell them the secret until they've tried it... or maybe just keep the secret.






		
		
		
		
