It never ceases to amaze me how little things can tell us so much... Like what just happened in a small New Jersey mall where two teen age boys, one black, one white, ended up in an argument and began to scuffle.  Nothing new here... just two boys fighting over something I'm sure wasn't worth fighting about.  All too often, there first course of action is to begin shoving and then punches soon follow... But that's not the worse of it.  No, I don't believe they should solve their issues with fist to cuffs.  No, I don't advocate violence to solve problems... whether it's children or adults... but what really bothers me is how the cops handled the situation. 
