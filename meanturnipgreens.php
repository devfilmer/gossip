    <p>


		 <br><br>
		Nothing in this world says summer like a zesty side of turnip greens... And today, we're going to show you how we like to prepare them.  So let's get ready to set the plate straight with some mean turnip greens.<br><br>
	  <ul>
		  <li>Hopefully, you have fresh turninps.  We pick them fresh... about a small plastic bag full... This equates to about a pot full of uncooked washed turnip greens.</li>
		  <li>Grab a big pot. Add five or six strips of bacon and cook on medium low.  Once the bacon is nearly done, proceed to the next step</li>
		  <li>add half a cup of sugar and half a cup of vinegar to the pot and set to low.  Stir occassionally</li>
		  <li>While the bacon, vinegar, and sugar are simmering, wash the turnip greens thoroughly to remove dirt, insects, and the rest of the great outdoors</li>
		  
		  <li>Get out your cutting board:  Roll and chop the greens.</li>
		  <li>Add the greens to the pot</li>
		  <li>Chop up two or three jalepeno pepper very thin and add to the pot</li>
		  <li>Chop up five or six banana pepper and add to the pot</li>
		  
			  <li>Stir and raise the heat back to medium low</li>
			  <li>If the liquid is low in the pot, add one quarter cup of water</li>
			  <li>Keep stirring until the greens are all dark.  This should take around 30 - 45 minutes</li>
			  <li>Serve and enjoy!</li>
			
		
		  
