It’s crazy when you think about it…. Watches you can communicate with, virtual currency, and now headsets that will take you to another world… These were outlandish ideas only believable in the movies.  But now, it’s real.  You can talk via Apple Watch.  You can buy any one of over 4000 virtual currencies and yes, you can visit worlds unknown and currently under construction.  It’s pretty scary if you think about it.  It’s like everything in “Ready Player One” is being made.  I wonder if the folks racing to build the metaverse ever really sat down and watched Ready Player One… It’s really a terrifying idea… that some day our reality would holistically be replace by a headset and our actual main existence would not be in the real world but in this make believe world made up by developers hell bent on entertaining us to a fault. </p> <br><br>

<p>
