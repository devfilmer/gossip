These concepts are no longer just scenes in the movies.  They’ve become the blueprint for the metaverse which is literally being built right before our very eyes… A perfect dystopia where everyone can recreate who they are and live out a fantasy with real currency albeit virtual/crypto currency… And to what end?  It’s an escape from our true physical connection to our planet which could lead to so many world issues that it become beyond what anyone could fathom. </p> <br><br>

<p>
<img src="images/axieinfinity.png"/>
</p>
<p>
Imagine a world where people are more concerned about their play to earn potential than they are about climate change…. a world where they’re more worried about their player level than their education level… a world where people value their real estate in the virtual worlds more then their real estate in the real world… a world where they are more concerned about their avatar’s appearance than their own personal hygiene.  I know what you’re thinking.  It will never get there.  But I beg to differ. When I was a kid, I remember spending hours sitting in front of Atari Space Invaders playing as if my very life depended on it…. flipping the game over and over and over again… And when I look at today’s games, they are far more immersive… far more addictive and far more difficult to put down… As a matter of fact, it’s even becoming easier to live in the virtual world than it is in our real world.  It’s an easy sell.  You just put on the headset, pull out your wallet, and a way you go.  You are only limited by your ability and your cash flow… </p> <br><br>
<p>
