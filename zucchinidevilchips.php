   <h1>Zucchini Devil Chips</h1>
	  
 
	<img src = "images/zucchinidevilchips/IMG_0574.jpg"/>
    <p>


		 <br><br>
		If there's one thing we know, everyone is going to stare when they see something odd... And today is that day... Feast your eyes on these Zucchini Devil Chips.  We can't wait for you to try this recipe<br><br>
	  <ul>
		  <li>Wash and Slice 2 zucchinis</li>
		  <li>Chop up a either one large bell pepper or two small ones very fine</li>
		  <li>Chop up 2 or 3 jalepeno peppers</li>
		  <li>Now let's get the chips in place.</li>
		  <ul>
			  <li>Spray Cookie Sheet lightly</li>
			  <li>Layer your zucchini chips on the cookie sheet</li>
			  <li>Drizzle with vegetable oil or olive oil</li>
			  <li>Sprinke season salt lightly. If you can see it, there's too much covering the chips</li>
			  <li>Follow up with a sprinkle of garlic power over the chips</li>
			 
			  <li>For a zesty pop, add a tad of tumeric if you like</li>
			  <li>Now, sprinkle the finely diced bell peppers over the chips</li>
			  <li>Oh yeah, and don't forget the pop... Sprinkle those jalepenos over them too.</li>
		  </ul>
		  <li>Preheat oven to 250</li>
		  <li>Place Cookie sheets with chips in the oven for 120 minutes</li>
		  <li>Check if chips are browning</li>
		  <li>If not, brown for 15 more minutes. You'll know when their ready because there should be no water or oil left.</li>
		  <li>Remove pans from oven</li>
		  <li>Sprinkle chips with shredded mozzerella cheese</li>
		  <li>Serve and Enjoy!</li>
		  
